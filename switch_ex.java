public class switch_ex{
    public  static void main(String args[]) {
        int number = 400;
        switch (number) {
            case 1: System.out.println("samosa");
                
                break;
            case 2 : System.out.println("burger");
                break;
            case 3 : System.out.println("mango shake");
                break;
            default: System.out.println("dream");
                break;
        }
    }
}
